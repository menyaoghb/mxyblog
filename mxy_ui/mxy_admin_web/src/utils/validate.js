export function validateTelOrFax(rule, value, callback) {
  if (!value) {
    callback();
  } else {
    const reg = /^(\d{3,4}-)?\d{7,8}$/;
    const isTel = reg.test(value);
    if (value.length < 10 || value.length > 13 || !isTel) {//判断传真字符范围是在10到13
      callback(new Error("座机或传真格式如:0376-1234567"));
    } else {
      callback();
    }
  }
};

